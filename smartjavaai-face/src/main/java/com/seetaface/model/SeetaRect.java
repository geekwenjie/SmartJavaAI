package com.seetaface.model;

/**
 * SeetaPointF
 * @author dwj
 */
public class SeetaRect {
    public int x;
    public int y;
    public int width;
    public int height;
    public float score;

    @Override
    public String toString() {
        return "SeetaRect{" +
                "x=" + x +
                ", y=" + y +
                ", width=" + width +
                ", height=" + height +
                ", score=" + score +
                '}';
    }
}
